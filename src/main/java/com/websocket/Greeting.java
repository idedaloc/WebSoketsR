package com.websocket;

public class Greeting {
	
	private String greeting;

	public Greeting() {
		super();
	}

	
	public Greeting(String greeting) {
		super();
		this.greeting = greeting;
	}


	public String getGreeting() {
		return greeting;
	}

	public void setGreeting(String greeting) {
		this.greeting = greeting;
	}
	
	

}
